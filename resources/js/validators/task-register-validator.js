'use strict';

/**
 * Task Register Validator
 */
function TaskValidator() {}
module.exports = TaskValidator;

/**
 * Validation funciton
 */
TaskValidator.validate = function validate(data) {
    const BaseValidator = require('JS-VALIDATORS/base-validator');

    const rules = {
        title: 'required|min:3|max:255'
    };
    let options = {
        lang : "fa",
        attributes : {
            title: 'عنوان'
        }
    };
    return BaseValidator.validate(data, rules, options);
};
